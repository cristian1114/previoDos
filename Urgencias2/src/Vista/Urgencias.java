    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser archivo = new JFileChooser();
        int select = archivo.showOpenDialog(this);
        
        if(select == JFileChooser.APPROVE_OPTION){
            File ruta = archivo.getSelectedFile();
            if(urgencia.cargarMedicos(ruta))
                JOptionPane.showMessageDialog(this, "Se han cargado los medicos exitosamente");
            else 
                JOptionPane.showMessageDialog(this, "Hubo un error, asegurate de seleccionar el archivo correcto");
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed

