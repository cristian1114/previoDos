    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        modelo.clear();
        LinkedList<Medico> medicos = urgencia.getMedicosEnGuardia();
        if(!medicos.isEmpty()){
        for (Medico m : medicos) {
            modelo.addElement(m.getNombre());
        }
        }else
            JOptionPane.showMessageDialog(this, "No se ha cargado la lista de medicos");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if(!txtDescripcion.getText().isEmpty() && !txtNombre.getText().isEmpty() && !txtIDasegurado.getText().isEmpty() &&
        !txtMedicoHabitual.getText().isEmpty() && !txtGradoDolor.getText().isEmpty()){
            
            
        if(urgencia.idRepetida(Integer.parseInt(txtIDasegurado.getText()))){
            JOptionPane.showMessageDialog(this, "El paciente ya se encuentra en sala de espera");
        }
        else if(!urgencia.idRepetida(Integer.parseInt(txtIDasegurado.getText()))){
            boolean ingreso = urgencia.insertarPaciente(txtDescripcion.getText(), new Paciente(txtNombre.getText(),Integer.parseInt(txtIDasegurado.getText()),
            txtMedicoHabitual.getText()), Integer.parseInt(txtGradoDolor.getText()));
            
            if(ingreso)
                JOptionPane.showMessageDialog(this, "El paciente Ingreso a sala de espera");
            else
            JOptionPane.showMessageDialog(this, "Lo sentimos el paciente no se encuentra asegurado");
        }
        }
        else
            JOptionPane.showMessageDialog(this, "Campos vacios!");
    }//GEN-LAST:event_btnIngresarActionPerformed

